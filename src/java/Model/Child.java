package Model;

/**
 *
 * @author Xuan Vinh
 */
public class Child {
    protected String childID;
    protected String name;
    protected int age;

    public Child() {
    }

    public Child(String childID, String name, int age) {
        this.childID = childID;
        this.name = name;
        this.age = age;
    }

    public String getChildID() {
        return childID;
    }

    public void setChildID(String childID) {
        this.childID = childID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
    
}
